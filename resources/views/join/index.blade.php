	$('#join_submit').click(function(){
		var is_submit = false;
		var stu_id = $.trim($('#stu_id').val())
		var name = $.trim($('#name').val())
		var phone = $.trim($('#phone').val())
		var stu_id_re = /^[0-9]{11}$/
		if(stu_id == '' || !stu_id_re.test(stu_id)) {
			$('#alert_stu_id').show()
			return
		} else {
			$('#alert_stu_id').hide()
		}
		if(name == '') {
			$('#alert_name').show()
			return
		} else {
			$('#alert_name').hide()
		}
		if(phone == '') {
			$('#alert_phone').show()
			return
		} else {
			$('#alert_phone').hide()
		}
		if(!is_submit) {
			is_submit = true
			$.ajax({
			    type: 'post',
                url: 'join/join',
                data: {
			        stu_id: stu_id,
                    name: name,
                    phone: phone
                },
                success: function(data) {
                    window.location.href = 'join/joinsuccess?stuId=' + data.stuId + '&check=' + data.check
                }

